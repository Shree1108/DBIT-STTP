# Atomic vector of type character.
str1 <- "TRUE"
print(str1)
print(class(str1))
# Atomic vector of type double.
dbl1 <- 12.523
print(dbl1)
print(class(dbl1))
# Atomic vector of type integer
int1 <- 128L
print(int1)
print(class(int1))
# Atomic vector of type logical.
logi1 <- TRUE 
print(logi1)
print(class(logi1))
# Atomic vector of type complex.
cmpl1 <- 2+3i
print(cmpl1)
print(class(cmpl1))
# Atomic vector of type raw.
raw1 <- charToRaw('hello ')
print(raw1)
print(class(raw1))
